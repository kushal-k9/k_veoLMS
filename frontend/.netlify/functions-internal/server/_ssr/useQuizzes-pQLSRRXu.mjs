async function listCourseQuizzes(courseId) {
	const { data } = await api.get(`/quizzes/course/${courseId}`);
	return data.data.quizzes;
}
async function listCourseQuizzesAdmin(courseId) {
	const { data } = await api.get(`/quizzes/course/${courseId}/admin`);
	return data.data.quizzes;
}
async function myAttempts(courseId) {
	const { data } = await api.get(`/quizzes/course/${courseId}/attempts/me`);
	return data.data.attempts;
}
async function submitQuiz(quizId, answers) {
	const { data } = await api.post(`/quizzes/${quizId}/attempts`, { answers });
	return data.data;
}
async function createQuiz(input) {
	const { data } = await api.post("/quizzes", input);
	return data.data.quiz;
}
async function updateQuiz(id, input) {
	const { data } = await api.patch(`/quizzes/${id}`, input);
	return data.data.quiz;
}
async function deleteQuiz(id) {
	await api.delete(`/quizzes/${id}`);
	return id;
}
function useCourseQuizzes(courseId, enabled = true) {
	return useQuery({
		queryKey: ["quizzes", courseId],
		queryFn: () => listCourseQuizzes(courseId),
		enabled: Boolean(courseId) && enabled
	});
}
function useCourseQuizzesAdmin(courseId) {
	return useQuery({
		queryKey: [
			"quizzes",
			"admin",
			courseId
		],
		queryFn: () => listCourseQuizzesAdmin(courseId),
		enabled: Boolean(courseId)
	});
}
function useMyAttempts(courseId) {
	return useQuery({
		queryKey: ["quiz-attempts", courseId],
		queryFn: () => myAttempts(courseId),
		enabled: Boolean(courseId)
	});
}
function useSubmitQuiz(courseId) {
	const qc = useQueryClient();
	return useMutation({
		mutationFn: ({ quizId, answers }) => submitQuiz(quizId, answers),
		onSuccess: () => qc.invalidateQueries({ queryKey: ["quiz-attempts", courseId] })
	});
}
function useSaveQuiz() {
	const qc = useQueryClient();
	return useMutation({
		mutationFn: ({ id, input }) => id ? updateQuiz(id, input) : createQuiz(input),
		onSuccess: () => qc.invalidateQueries({ queryKey: ["quizzes"] })
	});
}
function useDeleteQuiz() {
	const qc = useQueryClient();
	return useMutation({
		mutationFn: (id) => deleteQuiz(id),
		onSuccess: () => qc.invalidateQueries({ queryKey: ["quizzes"] })
	});
}
